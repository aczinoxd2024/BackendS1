import { NestFactory } from '@nestjs/core';
import { AppModule } from './app.module';
import { ValidationPipe } from '@nestjs/common';
import * as bodyParser from 'body-parser';
import { rawBodyMiddleware } from './common/middleware/raw-body.middleware';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  app.setGlobalPrefix('api');

  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  );

  // âœ… Webhook de Stripe â€” requiere rawBody y middleware personalizado
  app.use('/api/stripe/webhook', bodyParser.raw({ type: 'application/json' }));
  app.use(rawBodyMiddleware); // âœ… asigna rawBody para verificaciÃ³n
  app.use(bodyParser.json()); // âœ… para todas las demÃ¡s rutas normales

  app.enableCors({
    origin: [
      'http://localhost:4200',
      'https://backends1-production.up.railway.app',
      'http://backends1-production.up.railway.app',
      'https://proyectosis12025.netlify.app',
    ],
    methods: ['GET', 'POST', 'PUT', 'PATCH', 'DELETE', 'OPTIONS'],
    credentials: true,
  });

  const port: number = parseInt(process.env.PORT as string, 10) || 3000;
  await app.listen(port);
  console.log(`ðŸš€ Backend en ejecuciÃ³n â†’ http://localhost:${port}/api`);
}
bootstrap();
